package com.domain.lesson20Annotation.task.task2;
@ConfigClass(prefix="server")
public class ServerSettings {
    private String ip;
    private String port;

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }
}
